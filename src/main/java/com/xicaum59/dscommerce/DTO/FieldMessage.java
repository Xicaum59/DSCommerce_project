package com.xicaum59.dscommerce.DTO;

public class FieldMessage {
	
	private String fieldMessage;
	private String name;
	
	
	public FieldMessage(String fieldMessage, String name) {
		this.fieldMessage = fieldMessage;
		this.name = name;
	}


	public String getFieldMessage() {
		return fieldMessage;
	}


	public String getName() {
		return name;
	}
	
	

	
}
